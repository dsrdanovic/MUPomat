    private void btnPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjeniActionPerformed
        if (dokument == null) {
            JOptionPane.showMessageDialog(rootPane, "Prvo odaberite dokument!", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (listaAutomata.getSelectedValue() == null) {
            JOptionPane.showMessageDialog(rootPane, "Prvo odaberite automat!", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!kontrola()) {
            return;
        }

        dokument.setRedniBroj(Integer.parseInt(txtRedniBrojDokumenta.getText()));
        dokument.setNaziv(txtNazivDokumenta.getText());
        dokument.setOperater(operater);
        dokument.setAutomat((Automat) listaAutomata.getSelectedValue());
        obradaDokumenti.promjeniPostojeci(dokument);

        pocistiPolja();
        trazi();
    }//GEN-LAST:event_btnPromjeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if (dokument == null) {
            JOptionPane.showMessageDialog(rootPane, "Prvo odaberite dokument!", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Object[] options = {"Da",
            "Ne",};
        int rez = JOptionPane.showOptionDialog(rootPane,//parent container of JOptionPane
                "Sigurno obrisati",
                "Brisanje",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,//do not use a custom Icon
                options,//the titles of buttons
                null);//default button title
        if (rez == JOptionPane.YES_OPTION) {
            try {
                obradaDokumenti.obrisiPostojeci(dokument);
                trazi();
                pocistiPolja();
            } catch (SQLException ex) {
                Logger.getLogger(Automati.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnObrisiActionPerformed


