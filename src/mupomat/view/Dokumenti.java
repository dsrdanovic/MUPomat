    private void trazi() {

        List<Dokument> lista = obradaDokumenti.dohvatiIzBaze(txtUvjet.getText().trim());
        DefaultListModel<Dokument> m = new DefaultListModel<>();

        for (Dokument dokument : lista) {
            m.addElement(dokument);
        }
        listaDokumenata.setModel(m);
    }

    private boolean kontrola() {
        if (!kontrolaRedniBrojDokumenta()
                || !kontrolaNazivDokumenta()) {
            return false;
        }

        return true;
    }

    private void pocistiPolja() {
        listaDokumenata.clearSelection();
        DefaultListModel<Automat> m = (DefaultListModel<Automat>) listaAutomata.getModel();
        m.removeAllElements();
        txtRedniBrojDokumenta.setText("");
        txtNazivDokumenta.setText("");
        cmbAutomati.setSelectedIndex(0);
    }

    private boolean kontrolaRedniBrojDokumenta() {
        pattern = Pattern.compile(Kontrola.REDNI_BROJ_DOKUMENTA);
        matcher = pattern.matcher(txtRedniBrojDokumenta.getText().trim());

        Kontrola.resetirajPoljeGreske(txtRedniBrojDokumenta);
        if (!Kontrola.kontrolaPraznoPolje(txtRedniBrojDokumenta.getText(), txtRedniBrojDokumenta, "Ovo polje je obavezno! ", ikona)) {
            Kontrola.oznaciPoljeGreske(txtRedniBrojDokumenta);
            return false;
        }
        if (!matcher.matches()) {
            Kontrola.kontrolaUnosa(txtRedniBrojDokumenta, "<html>Netoƒçan unos.<br>Dopu≈°teno: [0-9]<br>(min.1 broj)</html> ", ikona);
            Kontrola.oznaciPoljeGreske(txtRedniBrojDokumenta);
            return false;
        }

        return true;

    }

    private boolean kontrolaNazivDokumenta() {
        pattern = Pattern.compile(Kontrola.NAZIV_DOKUMENTA);
        matcher = pattern.matcher(txtNazivDokumenta.getText().trim());

        Kontrola.resetirajPoljeGreske(txtNazivDokumenta);
        if (!Kontrola.kontrolaPraznoPolje(txtNazivDokumenta.getText(), txtNazivDokumenta, "Ovo polje je obavezno! ", ikona)) {
            Kontrola.oznaciPoljeGreske(txtNazivDokumenta);
            return false;
        }
        if (!matcher.matches()) {
            Kontrola.kontrolaUnosa(txtNazivDokumenta, "<html>Netoƒçan unos.<br>Dopu≈°teno: [a-≈æ,A-≈Ω,-,razmak]<br>(min.3 max.50 znakova)</html> ", ikona);
            Kontrola.oznaciPoljeGreske(txtNazivDokumenta);
            return false;
        }

        return true;
    }

    private void cmbAutomati() {
        ObradaAutomat oa = new ObradaAutomat();
        List<Automat> l = oa.dohvatiIzBaze("");
        DefaultComboBoxModel<Automat> m = new DefaultComboBoxModel<>();
        Automat a = new Automat();
        a.setSifra(0);
        a.setNaziv("--- Odaberite automat ---");
        m.addElement(a);
        for (Automat automat : l) {
            m.addElement(automat);
        }
        cmbAutomati.setModel(m);
    }

    private void postaviAutomate() {
        DefaultListModel<Automat> m = new DefaultListModel<>();

        for (Automat a : dokument.getAutomati()) {
            m.addElement(a);
        }
        listaAutomata.setModel(m);
    }
