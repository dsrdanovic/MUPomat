    private void listaDokumenataValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaDokumenataValueChanged
        if (evt.getValueIsAdjusting()) {
            return;
        }
        cmbAutomati.setSelectedIndex(0);
        dokument = (mupomat.model.Dokument) listaDokumenata.getSelectedValue();

        if (dokument == null) {
            return;
        }

        postaviAutomate();
        SimpleDateFormat df = new SimpleDateFormat(Kontrola.FORMAT_DATUMA_VRIJEME);
        lblDatumUnosa.setText(df.format(dokument.getDatumUnosa()));

        if (dokument.getDatumPromjene() != null) {
            lblDatumPromjene.setText(df.format(dokument.getDatumPromjene()));
        }

        txtRedniBrojDokumenta.setText(String.valueOf(dokument.getRedniBroj()));
        txtNazivDokumenta.setText(dokument.getNaziv());
    }//GEN-LAST:event_listaDokumenataValueChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (!kontrola()) {
            return;
        }

        if (cmbAutomati.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Niste odabrali automat!", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return;
        }

        dokument = new mupomat.model.Dokument();
        dokument.setRedniBroj(Integer.parseInt(txtRedniBrojDokumenta.getText()));
        dokument.setNaziv(txtNazivDokumenta.getText());
        dokument.setAutomat((Automat) cmbAutomati.getSelectedItem());
        dokument.setOperater(operater);
        dokument = obradaDokumenti.dodajNovi(dokument);
        pocistiPolja();
        trazi();

    }//GEN-LAST:event_btnDodajActionPerformed

