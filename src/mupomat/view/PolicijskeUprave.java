    private void trazi() {
        List<PolicijskaUprava> lista = obradaPolicijskaUprava.dohvatiIzBaze(txtUvjet.getText().trim());
        DefaultListModel<PolicijskaUprava> m = new DefaultListModel<>();

        for (PolicijskaUprava policijskaUprava : lista) {
            m.addElement(policijskaUprava);
        }
        listaPolicijskihUprava.setModel(m);

    }

    private void centrirajZaslon() {
        Dimension dimension = Toolkit.getDefaultToolkit().getScreenSize();
        int x = (int) ((dimension.getWidth() - this.getWidth()) / 2);
        int y = (int) ((dimension.getHeight() - this.getHeight()) / 2);
        this.setLocation(x, y);
        this.setResizable(false);

    }

