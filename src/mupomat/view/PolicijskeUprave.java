    private void listaPolicijskihUpravaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaPolicijskihUpravaValueChanged

        if (evt.getValueIsAdjusting()) {
            return;
        }

        policijskaUprava = (PolicijskaUprava) listaPolicijskihUprava.getSelectedValue();

        if (policijskaUprava == null) {
            return;
        }

        SimpleDateFormat df = new SimpleDateFormat(Kontrola.FORMAT_DATUMA_VRIJEME);
        lblDatumUnosa.setText(df.format(policijskaUprava.getDatumUnosa()));

        if (policijskaUprava.getDatumPromjene() != null) {
            lblDatumPromjene.setText(df.format(policijskaUprava.getDatumPromjene()));
        }

        txtNaziv.setText(policijskaUprava.getNaziv());
        txtNacelnik.setText(policijskaUprava.getNacelnik());
        txtSjediste.setText(policijskaUprava.getSjediste());
        txtAdresa.setText(policijskaUprava.getAdresa());
        txtTelefon.setText(policijskaUprava.getTelefon());
        txtInternetStranica.setText(policijskaUprava.getInternetstranica());


    }//GEN-LAST:event_listaPolicijskihUpravaValueChanged

