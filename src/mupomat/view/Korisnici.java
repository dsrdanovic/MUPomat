    private void btnDeaktivirajKorisnikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeaktivirajKorisnikaActionPerformed
        if (korisnik == null) {
            JOptionPane.showMessageDialog(rootPane, "Prvo odaberite aktivnog korisnika", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (listaNeaktivnihKorisnika.getSelectedIndex() != -1) {
            JOptionPane.showMessageDialog(rootPane, "Korisnik je već deaktiviran", "Greška", JOptionPane.ERROR_MESSAGE);
        }
        korisnik.setAktivan(false);

        obradaKorisnik.promjeniPostojeci(korisnik);
        pocistiPolja();
        trazi();
        traziNeaktivneKorisnike();
    }//GEN-LAST:event_btnDeaktivirajKorisnikaActionPerformed

