    private void listaAktivnihKorisnikaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaAktivnihKorisnikaValueChanged
        if (evt.getValueIsAdjusting()) {
            listaNeaktivnihKorisnika.clearSelection();
            
        }

        korisnik = (Korisnik) listaAktivnihKorisnika.getSelectedValue();
        if (korisnik == null) {
            return;
        }

        txtOib.setText(korisnik.getOib());
        txtIme.setText(korisnik.getIme());
        txtPrezime.setText(korisnik.getPrezime());
        txtKorisnickoIme.setText(korisnik.getKorisnickoIme());
        txtEmail.setText(korisnik.getEmail());

        SimpleDateFormat df = new SimpleDateFormat(Kontrola.FORMAT_DATUMA);
        txtDatumRegistracije.setText(df.format(korisnik.getDatumRegistracije()));

        SimpleDateFormat df1 = new SimpleDateFormat(Kontrola.FORMAT_VRIJEME);
        txtVrijemeRegistracije.setText(df1.format(korisnik.getDatumRegistracije()));
    }//GEN-LAST:event_listaAktivnihKorisnikaValueChanged

